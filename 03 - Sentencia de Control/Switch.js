let numero = 5;

switch (numero) {
    case 1:
        numero = 'Numero uno';
        break;
    case 2:
        numero = 'Numero dos';
        break;
    case 3: 
        numero = 'Numero tres';
        break;
    case 4:
        numero = 'Numero cuatro';
        break;
    default:
        numero = 'Caso no Encontrado';
}

console.log(numero);